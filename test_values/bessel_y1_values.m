function [ n_data, x, fx ] = bessel_y1_values ( n_data )

%*****************************************************************************80
%
%% BESSEL_Y1_VALUES returns some values of the Y1 Bessel function.
%
%  Discussion:
%
%    In Mathematica, the function can be evaluated by:
%
%      BesselY[1,x]
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    16 August 2004
%
%  Author:
%
%    John Burkardt
%
%  Reference:
%
%    Milton Abramowitz and Irene Stegun,
%    Handbook of Mathematical Functions,
%    US Department of Commerce, 1964.
%
%    Stephen Wolfram,
%    The Mathematica Book,
%    Fourth Edition,
%    Wolfram Media / Cambridge University Press, 1999.
%
%  Parameters:
%
%    Input/output, integer N_DATA.  The user sets N_DATA to 0 before the
%    first call.  On each call, the routine increments N_DATA by 1, and
%    returns the corresponding data; when there is no more data, the
%    output value of N_DATA will be 0 again.
%
%    Output, real X, the argument of the function.
%
%    Output, real FX, the value of the function.
%
  n_max = 16;

  fx_vec = [ ...
     -0.6458951094702027E+01, ...
     -0.7812128213002887E+00, ...
     -0.1070324315409375E+00, ...
      0.3246744247918000E+00, ...
      0.3979257105571000E+00, ...
      0.1478631433912268E+00, ...
     -0.1750103443003983E+00, ...
     -0.3026672370241849E+00, ...
     -0.1580604617312475E+00, ...
      0.1043145751967159E+00, ...
      0.2490154242069539E+00, ...
      0.1637055374149429E+00, ...
     -0.5709921826089652E-01, ...
     -0.2100814084206935E+00, ...
     -0.1666448418561723E+00, ...
      0.2107362803687351E-01 ];

  x_vec = [ ...
      0.1E+00, ... 
      1.0E+00, ... 
      2.0E+00, ... 
      3.0E+00, ... 
      4.0E+00, ... 
      5.0E+00, ... 
      6.0E+00, ... 
      7.0E+00, ... 
      8.0E+00, ... 
      9.0E+00, ... 
     10.0E+00, ... 
     11.0E+00, ... 
     12.0E+00, ... 
     13.0E+00, ... 
     14.0E+00, ... 
     15.0E+00  ];

  if ( n_data < 0 )
    n_data = 0;
  end

  n_data = n_data + 1;

  if ( n_max < n_data )
    n_data = 0;
    x = 0.0;
    fx = 0.0;
  else
    x = x_vec(n_data);
    fx = fx_vec(n_data);
  end

  return
end

function area = sphere01_area ( )

%*****************************************************************************80
%
%% SPHERE01_AREA returns the area of the unit sphere.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license. 
%
%  Modified:
%
%    02 January 2014
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Output, real AREA, the area of the unit sphere.
%
  r = 1.0;
  area = 4.0 * pi * r * r;

  return
end
